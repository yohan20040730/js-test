var perkalian = 0
var pembagian = 0
var penambahan = 0
var pengurangan = 0

function calculate(a, b) {
    perkalian = a * b
    pembagian = a / b
    penambahan = a + b
    pengurangan = a - b
}

calculate(3,2)
console.log(perkalian);

calculate(10, 2)
console.log(pembagian);

calculate(76, 12)
console.log(penambahan);

calculate(789, 10)
console.log(pengurangan);
